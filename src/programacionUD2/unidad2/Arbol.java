package programacionUD2.unidad2;

public class Arbol {
	public static void main(String[] args) {
		String cadena=" ";
	
		for (int i = 4 ; i <= 2 ; i--) {
			cadena = cadena +"*";
			System.out.println(cadena);
			for (int j=1; j <= 1; j++)
				cadena+="*";
				System.out.println(cadena);
			
	}
	}
}
